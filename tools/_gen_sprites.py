# ============================================================ EMIT

def main():
    counts = {
        16: sum(1 for v in SPR.values() if len(v) == 256),
        8: sum(1 for v in SPR.values() if len(v) == 64),
    }
    body = json.dumps(SPR, indent=2)
    out = ROOT / "js" / "data-sprites.js"
    out.write_text(f"const SPRITES = /*JSON*/{body};\n", encoding="utf-8")
    print(f"wrote {out} — {len(SPR)} sprites ({counts[16]}x 16px, {counts[8]}x 8px)")


if __name__ == "__main__":
    main()
