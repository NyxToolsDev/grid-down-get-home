"""Generator for js/data-sprites.js (Grid Down: Get Home).

Authoring tool for the art-sprites agent. Builds all required 16x16 entity
sprites, 8x8 UI glyphs and 8x8 item icons as '0123.' pixel strings and emits
the strict-JSON data file.
Run:  python tools/_gen_sprites.py
Then: node tools/validate-data.mjs sprites
      python tools/preview-art.py sprites
"""
import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
SPR = {}


def S16(name, rows):
    assert len(rows) == 16, f"{name}: need 16 rows, got {len(rows)}"
    for i, r in enumerate(rows):
        assert len(r) == 16, f"{name}: row {i} is {len(r)} chars: {r!r}"
        assert all(c in "0123." for c in r), f"{name}: bad char in row {i}"
    SPR[name] = "".join(rows)


def S8(name, rows):
    assert len(rows) == 8, f"{name}: need 8 rows, got {len(rows)}"
    for i, r in enumerate(rows):
        assert len(r) == 8, f"{name}: row {i} is {len(r)} chars: {r!r}"
        assert all(c in "0123." for c in r), f"{name}: bad char in row {i}"
    SPR[name] = "".join(rows)


